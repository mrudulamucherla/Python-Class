'''  Write a  program to enter two numbers 
        and perform all arithmetic operations.
'''
a= 68
b=12

# sum
sum= a+b
print("sum of a,b:",sum)

# difference
diff= a-b
print("difference of a,b :",diff)

# multiplication
mul= a*b
print("multiplication of a,b :",mul)

# division
div= a/b
print("division of a,b: ",div)

# floor or integer division
int_div= a//b
print("integer division of a,b :",int_div)

# remainder
rem= a%b
print("remainder of a,b :",rem)

from math import pow, sqrt
# power
x= pow(5,3)
print(x)

#square root 
z= sqrt(289)
print(z)

